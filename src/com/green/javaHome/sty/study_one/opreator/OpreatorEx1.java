package com.green.javaHome.sty.study_one.opreator;

public class OpreatorEx1 {
    public static void main(String[] args) {
        int i = 5;

        i++;
        System.out.println(i);

        i = 5;
        ++i;
        ++i;
        System.out.println(i);

        i = 5;
        --i;
        System.out.println(i);


    }
}
